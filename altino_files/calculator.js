function Calculator(table) {
  this.getLiters = function(query) {
    var [cm, decimals] = query.replace(",", ".").split(".");
    cm = parseInt(cm);

    if (cm > 191 || cm < 1) return;

    var liters = table[cm];
    var nextLiters = table[cm + 1];

    if (decimals) {
      if (decimals.length > 1) return;

      decimals = parseInt(decimals);

      // figure it out how much a decimal is worth
      if (nextLiters) {
        var decimalUnit = (nextLiters - liters) / 10;
      } else {
        var decimalUnit = 1;
      }

      return this.format(liters + (decimals * decimalUnit));
    }

    return this.format(liters);
  }

  this.format = function(number) {
    var [integer, decimals] = number.toString().split('.');
    integer = integer.replace(/\B(?=(\d{3})+(?!\d))/g, ".");
    return [integer, decimals].join(',');
  }
}

